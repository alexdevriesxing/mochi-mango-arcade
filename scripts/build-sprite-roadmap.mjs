#!/usr/bin/env node
// Generate the sprite rollout roadmap from live data.
//
// Pairs the 146 delivered spritesheets against games.json, splits the work into
// batches, and writes both a machine-readable manifest (consumed by the slicing
// batches) and a human-readable markdown plan.
//
//   node scripts/build-sprite-roadmap.mjs <sheets.txt>

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { join } from 'node:path';

const ROOT = new URL('..', import.meta.url).pathname.replace(/^\/([A-Za-z]:)/, '$1');
const BATCH_SIZE = 15;

const games = JSON.parse(readFileSync(join(ROOT, 'public/assets/data/games.json'), 'utf8'));

// Must stay identical to the slug used by mmengine.js:51, since that is what
// decides which sprite file a game reaches for at runtime.
const slug = (s) =>
  s.toLowerCase().normalize('NFD').replace(/[̀-ͯ]/g, '')
    .replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '');

const sheets = readFileSync(process.argv[2], 'utf8').trim().split('\n')
  .map((l) => l.trim()).filter(Boolean)
  .map((file) => {
    const [, num, raw] = file.match(/^(\d+)_(.+)\.png$/);
    return { file, num: +num, name: raw.replace(/_/g, ' '), slug: slug(raw) };
  })
  .sort((a, b) => a.num - b.num);

const byMascot = new Map();
for (const g of games) {
  if (!g.mascot) continue;
  const k = slug(g.mascot);
  if (!byMascot.has(k)) byMascot.set(k, []);
  byMascot.get(k).push(g);
}

const tokens = (s) => s.split('-').filter((t) => t.length > 2);
function matchMascot(sheet) {
  if (byMascot.has(sheet.slug)) return { key: sheet.slug, how: 'exact' };
  for (const key of byMascot.keys()) {
    const a = tokens(sheet.slug), b = tokens(key);
    const shared = a.filter((t) => b.includes(t));
    if (shared.length && (shared.length === a.length || shared.length === b.length)) {
      return { key, how: 'fuzzy' };
    }
  }
  return null;
}

const entries = sheets.map((s) => {
  const m = matchMascot(s);
  const gs = m ? byMascot.get(m.key) : [];
  const universes = [...new Set(gs.map((g) => g.universe))];
  return {
    ...s,
    mascot: m ? m.key : null,
    match: m ? m.how : 'orphan',
    games: gs.length,
    gameSlugs: gs.map((g) => g.slug),
    universes,
    modes: [...new Set(gs.map((g) => g.engine))],
  };
});

// Highest-leverage first: characters that already drive real games get sliced
// and wired before the orphans, so the pipeline is proven on live surfaces
// before we start reassigning mascots.
const ordered = [...entries].sort((a, b) => {
  if (!!a.mascot !== !!b.mascot) return a.mascot ? -1 : 1;
  if (b.games !== a.games) return b.games - a.games;
  return a.num - b.num;
});

const batches = [];
for (let i = 0; i < ordered.length; i += BATCH_SIZE) {
  const items = ordered.slice(i, i + BATCH_SIZE);
  batches.push({
    n: batches.length + 1,
    items,
    games: items.reduce((t, e) => t + e.games, 0),
    orphans: items.filter((e) => !e.mascot).length,
  });
}

mkdirSync(join(ROOT, 'docs'), { recursive: true });
writeFileSync(
  join(ROOT, 'docs/sprite-roadmap.json'),
  JSON.stringify({ generated: new Date().toISOString().slice(0, 10), batchSize: BATCH_SIZE, entries, batches: batches.map((b) => ({ n: b.n, files: b.items.map((i) => i.file) })) }, null, 2)
);

const matched = entries.filter((e) => e.mascot);
const covered = new Set(matched.flatMap((e) => e.gameSlugs));

const md = [];
md.push('# Sprite Rollout Roadmap');
md.push('');
md.push('_Generated by `scripts/build-sprite-roadmap.mjs` — do not hand-edit; re-run after changing `games.json`._');
md.push('');
md.push('## Position');
md.push('');
md.push(`- **146** spritesheets delivered (1920×1920 RGBA, background already at alpha 0).`);
md.push(`- **${matched.length}** map to an existing mascot by name; **${entries.length - matched.length}** are orphans awaiting a mascot remap.`);
md.push(`- **${covered.size} / ${games.length}** games are covered by a name-matched character today.`);
md.push('');
md.push('Sheets are laid out in semantic rows (idle → walk → run → jump/dash → action → emote → face)');
md.push('but row and column counts vary per sheet, so frames are segmented on the alpha channel');
md.push('rather than sliced on a fixed grid. See `scripts/slice-spritesheet.mjs`.');
md.push('');
md.push('## Batches');
md.push('');
md.push('| Batch | Characters | Games touched | Orphans | Lead characters |');
md.push('|---:|---:|---:|---:|---|');
for (const b of batches) {
  const lead = b.items.slice(0, 3).map((i) => i.name).join(', ');
  md.push(`| ${b.n} | ${b.items.length} | ${b.games} | ${b.orphans} | ${lead}${b.items.length > 3 ? ', …' : ''} |`);
}
md.push('');
for (const b of batches) {
  md.push(`### Batch ${b.n}`);
  md.push('');
  md.push('| # | Character | Mascot | Match | Games | Universes |');
  md.push('|---:|---|---|---|---:|---|');
  for (const e of b.items) {
    md.push(`| ${String(e.num).padStart(3, '0')} | ${e.name} | ${e.mascot || '—'} | ${e.match} | ${e.games} | ${e.universes.join(', ') || '—'} |`);
  }
  md.push('');
}

writeFileSync(join(ROOT, 'docs/SPRITE-ROADMAP.md'), md.join('\n'));
console.log(`${entries.length} sheets -> ${batches.length} batches`);
console.log(`matched ${matched.length}, orphans ${entries.length - matched.length}, games covered ${covered.size}/${games.length}`);
console.log('wrote docs/SPRITE-ROADMAP.md + docs/sprite-roadmap.json');
